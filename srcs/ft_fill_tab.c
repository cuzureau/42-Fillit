#include "fillit.h"

char	**ft_fill_tab(char **dst)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (j < (int)ft_strlen(dst[0]))
	{
		while (dst[j][i])
		{
			dst[j][i] = '.';
			i++;
		}
		i = 0;
		j++;
	}
	return (dst);
}
